import assert from "node:assert/strict";
import { spawnSync } from "node:child_process";
import { existsSync, readdirSync, readFileSync, statSync } from "node:fs";
import { join } from "node:path";
import { test } from "node:test";
import { fileURLToPath } from "node:url";

const root = fileURLToPath(new URL("..", import.meta.url));

const scannedRoots = [
  "README.md",
  "docs",
  "sdk/README.md",
  "cli/README.md",
  "mcp-server/README.md",
];

const banned = [
  ["generated Speakeasy prose", /\bgenerated by Speakeasy\b/i],
  ["Speakeasy codegen prose", /\bCode generated by Speakeasy\b/i],
  ["old SDK class import", /import\s+\{\s*Plaky115\s*\}\s+from\s+["']plaky115["']/],
  ["old SDK class constructor", /\bnew\s+Plaky115\s*\(/],
  ["old core export", /\bPlaky115(Core|McpCore)\b/],
  ["old core import", /from\s+["']plaky115\/core\.js["']/],
  ["old funcs import", /from\s+["']plaky115\/funcs\//],
  ["old plaky wrapper import", /from\s+["']plaky115\/plaky["']/],
  ["old wrapper factory", /\bcreatePlakyClient\b/],
  ["old CLI api-key flag", /--api-key-auth\b/],
  ["old CLI live flag", /\bdoctor\s+--live\b/],
  ["old CLI output flag", /\s-o\s+json\b/],
  ["old grouped workspace command", /\bplaky115\s+workspace\s+map\b/],
  ["old grouped spaces command", /\bplaky115\s+spaces\s+list\b/],
  ["old grouped boards command", /\bplaky115\s+boards\s+list\b/],
  ["old grouped items command", /\bplaky115\s+items\s+list\b/],
  ["removed MCP curated tool", /\bplaky_list_items\b/],
];

test("public docs do not reference stale generated-era surfaces", () => {
  const failures = [];
  for (const rel of markdownFiles()) {
    const text = readFileSync(join(root, rel), "utf8");
    for (const [label, regex] of banned) {
      if (regex.test(text)) failures.push(`${rel}: ${label}`);
    }
  }
  assert.deepEqual(failures, []);
});

test("stale generated docs, examples, and local plans are not tracked", () => {
  const removed = [
    "docs/superpowers",
    "sdk/docs",
    "sdk/examples",
    "mcp-server/docs",
    "mcp-server/examples",
    "cli/USAGE.md",
    "sdk/FUNCTIONS.md",
    "sdk/USAGE.md",
    "mcp-server/FUNCTIONS.md",
    "mcp-server/USAGE.md",
  ];
  const result = spawnSync("git", ["ls-files", "--", ...removed], {
    cwd: root,
    encoding: "utf8",
  });
  assert.equal(result.status, 0, result.stderr);
  assert.equal(result.stdout.trim(), "");
});

function markdownFiles() {
  const files = [];
  for (const rel of scannedRoots) {
    const abs = join(root, rel);
    if (!existsSync(abs)) continue;
    if (statSync(abs).isDirectory()) {
      walkMarkdown(rel, files);
    } else if (rel.endsWith(".md")) {
      files.push(rel);
    }
  }
  return files.sort();
}

function walkMarkdown(rel, files) {
  const abs = join(root, rel);
  for (const entry of readdirSync(abs, { withFileTypes: true })) {
    const child = `${rel}/${entry.name}`;
    if (entry.isDirectory()) {
      walkMarkdown(child, files);
    } else if (entry.isFile() && entry.name.endsWith(".md")) {
      files.push(child);
    }
  }
}
